class Complex {

    private double real;
    private double imaginary;

    double getReal() {
        return this.real;
    }
    void setReal(double newReal) {
        this.real = newReal;
    }
    double getImaginary() {
        return this.imaginary;
    }
    void setImaginary(double newImaginary) {
        this.imaginary = newImaginary;
    }
}